namespace Genny.Constants;

/// <summary>
/// Constants for common directory names used throughout the application.
/// </summary>
public static class DirectoryConstants
{
    /// <summary>
    /// The pages directory name where HTML pages are stored.
    /// </summary>
    public const string Pages = "pages";

    /// <summary>
    /// The layouts directory name where layout templates are stored.
    /// </summary>
    public const string Layouts = "layouts";

    /// <summary>
    /// The partials directory name where partial templates are stored.
    /// </summary>
    public const string Partials = "partials";

    /// <summary>
    /// The public directory name where public assets are stored.
    /// </summary>
    public const string Public = "public";

    /// <summary>
    /// The build directory name where the generated site is output.
    /// </summary>
    public const string Build = "build";
}
